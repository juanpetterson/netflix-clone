export default {
  getMedias() {
    return [...medias];
  },

  getAllMedias() {
    return [...allMedias];
  },

  getMedia(id) {
    const media = allMedias.filter(media => media.id === id)[0];

    return media;
  },

  getWatchingMedias(params) {
    let keepWatching = [];
    params.forEach((id, index) => {
      keepWatching[index] = allMedias.filter(media => media.id === id)[0];
    });

    if (keepWatching) {
      keepWatching = {
        metric: 'Continue Watching',
        data: keepWatching,
      };
    }

    return keepWatching;
  },
};
